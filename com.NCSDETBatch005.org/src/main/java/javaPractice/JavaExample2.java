package javaPractice;

class JavaExample2 {

	public static void main(String[] args) {

		int a = 10;
		System.out.println(a); // 10

		int b = ++a;
		System.out.println(b); // 11

		int c = b++;
		System.out.println(c); // 11

		int d = --c;
		System.out.println(d); // 10

		int e = d--;
		System.out.println(e); // 10
	}

}